package rp.plf;

public class C4 {
    public String nom, lien;

    public C4(String nom, String lien) {
        this.nom = nom;
        this.lien = lien;
    }
    public C4(){

    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getLien() {
        return lien;
    }

    public void setLien(String lien) {
        this.lien = lien;
    }
}
